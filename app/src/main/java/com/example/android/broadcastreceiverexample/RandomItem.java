package com.example.android.broadcastreceiverexample;

class RandomItem {
    private int number;
    private int color;

    public RandomItem(int number, int color) {
        this.number = number;
        this.color = color;
    }

    public int getNumber() {
        return number;
    }

    public int getColor() {
        return color;
    }
}
